user_input = {}
for feat in features:
    opts = list(encoders[feat].classes_)
    translated_opts = [f"{opt} ‚Äî {value_translations[feat].get(opt, opt)}" for opt in opts]

    if feat == "spore-print-color":
        translated_opts.insert(0, "‚ùå –ù–µ —É–∫–∞–∑—ã–≤–∞—Ç—å")

    selected = st.selectbox(feature_labels[feat], translated_opts)

    if feat == "spore-print-color" and selected == "‚ùå –ù–µ —É–∫–∞–∑—ã–≤–∞—Ç—å":
        user_input[feat] = None
    else:
        user_input[feat] = selected.split(" ‚Äî ")[0]

